package main

import "fmt"

func main() {
	add := 1 + 2
	fmt.Println(add)

	subtract := 6 - 3
	fmt.Println(subtract)

	remainder := 12 % 4
	fmt.Println(remainder)

	// increment operator
	var1 := 1
	var1++
	fmt.Println(var1)

	// decrement operator
	var1--
	fmt.Println(var1)

	var1 += 10
	fmt.Println(var1)
}
